#pragma once
#include "String.h"
#include "LinkedList.h"
using namespace std;


class MovieNode;


class GenreNode 
{
private:
String genreName;
LinkedList<MovieNode*> movies;  

public:
GenreNode() 
{
}

GenreNode(const String& name)
{
genreName = name;
}

void setName(const String& name) 
{
genreName = name; 
}


const String& getName() const 
{ 
return genreName; 
}
const LinkedList<MovieNode*>& getMovies() const 
{ 
return movies; 
}


void addMovie(MovieNode* movie) 
{
if (movie != nullptr) 
{
movies.append(movie);

}

}


int getMovieCount() const 
{
return movies.getSize();

}


double getAverageRating() const 
{
if (movies.isEmpty()) return 0.0;

double totalRating = 0.0;

int count = 0;

for (auto it = movies.begin(); it != movies.end(); ++it) 
{
MovieNode* movie = *it;

totalRating += movie->getImdbScore();
count++;

}

return totalRating/count;

}

void display() const 
{
cout<<"========================================"<<endl;
cout<<"Genre: "<<genreName<<endl;

cout<<"Number of Movies: "<<movies.getSize()<<endl;


cout<<"Average Rating: "<<getAverageRating()<<"/10"<<endl;

cout<<"========================================"<<endl;

}

void displayMovies() const 
{
cout<<endl<<"Movies in genre '"<<genreName<<"':"<<endl;
cout<<"+++++++++++++++++++++++++++++"<<endl;

int count = 1;
for (auto it = movies.begin(); it != movies.end(); ++it) 
{
MovieNode* movie = *it;

cout<<count++<<". ";


movie->displayBrief();


}

if (movies.isEmpty()) 
{
cout<<"No movies ."<<endl;

}

}

bool operator==(const GenreNode& other) const 
{
return genreName == other.genreName;

}

bool operator!=(const GenreNode& other) const 
{
return genreName != other.genreName;

}

};
